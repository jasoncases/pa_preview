export const currentYear1Buffer = outputYearString(getYears(1));

/**
 *
 * @param {int} buffer number of years to either side of current year, ex current year = 2019, buffer = 1, dropdown shows 2018, 2019, 2020
 */
function getYears(buffer) {
   const year = new Date().getFullYear();
   const output = [year];

   for (let ii = 1; ii <= buffer; ii++) {
      output.push(year + ii);
      output.push(year - ii);
   }
   return output.sort();
}

function outputYearString(years) {
   let string = '';
   const currentYear = new Date().getFullYear();
   years.forEach(yr => {
      if (yr === currentYear) {
         string += `<option value="${yr}" selected>${yr}</option>`;
      } else {
         string += `<option value="${yr}">${yr}</option>`;
      }
   });
   return string;
}
